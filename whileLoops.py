def cuenta_regresiva(n):
    while n > 0:
        print(n)
        n -= 1

print('>>> cuenta_regresiva(3)')
cuenta_regresiva(3)

print('>>> cuenta_regresiva(30)')
cuenta_regresiva(30)

print('>>>')

